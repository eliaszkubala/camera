public class PointFloat {
    float x;
    float y;

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public PointFloat(float x, float y) {
        this.x = x;
        this.y = y;
    }

}
